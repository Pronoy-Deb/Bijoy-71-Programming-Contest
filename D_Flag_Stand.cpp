const ll M = 1e9 + 7, N = 2e5 + 5;

void test(int32_t tc) {
    cout << fixed << setprecision(8);
    long double H, h; cin >> H >> h;
    long double hyp = H - h;
    if (h > hyp) cout << 0;
    else cout << acos(h / hyp) * (180.0 / M_PI);
    cout << '\n';
}

int32_t main() {
    cin.tie(0)->sync_with_stdio(0); cin.exceptions(ios::failbit | ios::badbit);
    int32_t tc = 0, tt = 1;
    cin >> tt;
    while (tc++ < tt) test(tc);
    return 0;
}